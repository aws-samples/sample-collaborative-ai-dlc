#!/usr/bin/env node
// Regenerate lambda/shared/upstream-artifact-ids.json from the pinned
// awslabs/aidlc-workflows ref — run on EVERY aidlc_repo_ref bump
// (terraform/variables.tf), then commit the result.
//
//   node scripts/snapshot-upstream-artifacts.mjs [ref]
//
// The snapshot is the pin-drift guard for the typed-extraction registry:
// artifact-structure-contract.test.js asserts every registry key is a real
// upstream artifact id, so a ref bump that renames `stories`/`requirements`/…
// fails CI instead of silently disabling structure contracts + extraction.
//
// Fetches stage frontmatter over the GitHub raw API (same repo the seed job
// tarballs); no auth needed for the public repo.

import { writeFile, readFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = 'awslabs/aidlc-workflows';
const STAGES_DIR = 'core/aidlc-common/stages/';
const OUT = path.join(
  path.dirname(fileURLToPath(import.meta.url)),
  '..',
  'lambda/shared/upstream-artifact-ids.json',
);

const refFromTerraform = async () => {
  const tf = await readFile(
    path.join(path.dirname(fileURLToPath(import.meta.url)), '..', 'terraform/variables.tf'),
    'utf8',
  );
  const m = /variable "aidlc_repo_ref"[\s\S]*?default\s*=\s*"([^"]+)"/.exec(tf);
  if (!m) throw new Error('could not find aidlc_repo_ref default in terraform/variables.tf');
  return m[1];
};

const fetchJson = async (url) => {
  const res = await fetch(url, { headers: { 'user-agent': 'aidlc-snapshot' } });
  if (!res.ok) throw new Error(`${url} -> ${res.status}`);
  return res.json();
};

const fetchText = async (url) => {
  const res = await fetch(url, { headers: { 'user-agent': 'aidlc-snapshot' } });
  if (!res.ok) throw new Error(`${url} -> ${res.status}`);
  return res.text();
};

const producesFromFrontmatter = (text) => {
  const fm = /^---\n([\s\S]*?)\n---/.exec(text)?.[1];
  if (!fm) return [];
  const block = /^produces:\n((?: {2}- .*\n?)*)/m.exec(fm)?.[1] ?? '';
  return [...block.matchAll(/^ {2}- (.+)$/gm)].map((m) => m[1].trim());
};

const main = async () => {
  const ref = process.argv[2] ?? (await refFromTerraform());
  console.error(`snapshotting ${REPO}@${ref} …`);
  const tree = await fetchJson(`https://api.github.com/repos/${REPO}/git/trees/${ref}?recursive=1`);
  const stagePaths = tree.tree
    .map((t) => t.path)
    .filter((p) => p.startsWith(STAGES_DIR) && p.endsWith('.md'))
    .toSorted();
  if (!stagePaths.length) throw new Error(`no stage files under ${STAGES_DIR} at ${ref}`);

  const producesByStage = {};
  for (const p of stagePaths) {
    const text = await fetchText(`https://raw.githubusercontent.com/${REPO}/${ref}/${p}`);
    const produces = producesFromFrontmatter(text);
    if (produces.length) producesByStage[path.basename(p, '.md')] = produces;
  }
  const artifactIds = [...new Set(Object.values(producesByStage).flat())].toSorted();

  await writeFile(
    OUT,
    `${JSON.stringify(
      {
        _comment:
          'Artifact ids produced by the pinned awslabs/aidlc-workflows stages. GENERATED by scripts/snapshot-upstream-artifacts.mjs - regenerate on every aidlc_repo_ref bump (terraform/variables.tf). The pin-drift test (artifact-structure-contract.test.js) asserts every extraction-registry key exists here, so a ref bump that renames artifacts fails CI instead of silently disabling typed extraction.',
        ref,
        artifactIds,
        producesByStage,
      },
      null,
      2,
    )}\n`,
  );
  console.error(
    `wrote ${OUT}: ${artifactIds.length} artifact ids from ${Object.keys(producesByStage).length} stages`,
  );
};

main().catch((e) => {
  console.error(e.message);
  process.exit(1);
});
